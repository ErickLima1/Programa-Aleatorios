    public void initComplementosMostraTabela() {
        this.setLocationRelativeTo(null);
        EntityManager em = new ConnectionFactory().getConnection();  
     motoDAO motos = new motoDAO();
     List<Motos> TabelaMotos = new ArrayList();
     DefaultTableModel tabela = new DefaultTableModel();
     this.jTableMotos.setModel(tabela);
      
        tabela.addColumn("Id Placa Final");
        tabela.addColumn("Nome do Motos");
        tabela.addColumn("Ano");
        tabela.addColumn("Qtd De Motos");
        tabela.addColumn("Cor da Motos");
        tabela.addColumn("Cambio");
        tabela.addColumn("Valor");
        
      for(Motos c: motos.getBuscarTodos()) {
          tabela.addRow(
          new Object[] {
              c.getIdfinalplaca(),
              c.getNomemotos(),
              c.getAno(),
              c.getQuantidademoto(),
              c.getCor(),
              c.getCambio(),
              c.getValor()
          }
          );
      }
    }

